import 'dart:io';

void main (){
    
    // for even numbers
    for (int i = 0 ; i <= 100 ; i++){
       if (i%2== 0){
         print("Even numbers are $i");
       }

    }
    
   // for ODD numbers
    for (int i = 0 ; i <= 100 ; i++){
       if (i%2== 1){
         print("ODD numbers are $i");
       }

    }


}